
## Base code containts cor.test
# https://www.rdocumentation.org/packages/stats/versions/3.6.2/topics/cor.test

# x, y numeric vectors of data values. x and y must have the same length.
# One of "pearson", "kendall", or "spearman", can be abbreviated.

cor.test(x, y, method = "pearson") 
cor.test(x, y, method = "spearman") 